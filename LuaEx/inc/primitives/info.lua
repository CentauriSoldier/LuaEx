--[[!
@fqxn LuaEx.Primitives
@desc Primitives are custom-designed primitive shapes. They're something between a table and class.
<br>While they have some class-like behavior, they are not classes and are as simple and as <strong>fast</strong> as possible.
<br>
<br>
They're designed to be used in high-speed environments such as game development.
<br>While providing a system that performs autocalulations, they focus on speed over convenience while still offering some convenience.
<br>
<br>
In name, unlike classes, they are lowercase (e.g., <strong>circle</strong>, <strong>line</strong>, etc.).
!]]
